num1 = int(input("Enter first number: ")) # "43" -> 43
num2 = int(input("Enter second number: "))

print(f"Sum of {num1} and {num2} is {num1+num2}")
print(f"Sub of {num1} and {num2} is {num1-num2}")
print(f"Mul of {num1} and {num2} is {num1*num2}")
print(f"Divide (/) of {num1} and {num2} is {num1/num2}")
print(f"Divide (without decimal) of {num1} and {num2} is {int(num1/num2)}")
print(f"Divide (//) of {num1} and {num2} is {num1//num2}")
print(f"Divide (%) of {num1} and {num2} is {num1%num2}")
